#pragma mark - Segues

- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    if ([[segue identifier] isEqualToString:@"SelectDevice"]) {
        NSIndexPath *indexPath = [self.tableView indexPathForSelectedRow];

        DeviceListTableViewController *controller = (DeviceListTableViewController *)[[segue destinationViewController] topViewController];
        controller.managedObjectContext = self.managedObjectContext;
        controller.choiceInMasterView = indexPath.row;
        controller.navigationItem.leftBarButtonItem = self.splitViewController.displayModeButtonItem;
        controller.navigationItem.leftItemsSupplementBackButton = YES;
    }
    else if ([[segue identifier] isEqualToString:@"showSettings"]) {
        [[segue destinationViewController]setManagedObjectContext:self.managedObjectContext];
    }
}



